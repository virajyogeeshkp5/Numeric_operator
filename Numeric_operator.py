# Numeric operator
a=10
b=5
# addition
print("a+b:",a+b)
# subtraction
print("a-b:",a-b)
# division
print("a/b:",a/b)
# modulus
print("a%b:",a%b)
# exponent
print("a**b:",a**b)
# multiplication
print("a*b",a*b)
# floor division
print("a//b:",a//b)
# floor division exponent
a//=3
print("a//=3:",a)
# remainder assignment
a%=2
print("a%=2:",a)
# exponent assignment
a**=3
print("a**=3:",a)
